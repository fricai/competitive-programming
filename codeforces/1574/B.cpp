bool solve() {
	int a[3], m; cin >> a[0] >> a[1] >> a[2] >> m;
	sort(a, a + 3);
	return  a[2] - (a[0] + a[1] + 1) <= m && m <= a[0] + a[1] + a[2] - 3;
}

