bool solve() {
	// 1 if Alice wins, 0 if Bob
	int n; cin >> n;
	if (n % 2 != 0) return 0;
	if ((n & (n - 1)) != 0) return 1;
	int x = 0;
	while (n != 0) ++x, n /= 2;
	return x & 1;
}

signed main() {
	ios::sync_with_stdio(false);
	cin.tie(nullptr);

	int t; cin >> t;
	while (t--) cout << (solve() ? "Alice" : "Bob") << '\n';	
}